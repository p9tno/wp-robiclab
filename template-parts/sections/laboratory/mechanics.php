                    <p>Установите желоб на подставки на ровной горизонтальной поверхности так, чтобы шарик, помещенный в любую его точку, оставался в покое, то есть находился в состоянии безразличного равновесия.</p>
                    <p>В этом состоянии силы, действующие на шарик, взаимно уравновешены, а их равнодействующая равна нулю. Следовательно, можно считать, что движение шарика происходит в инерциальной системе отсчета.</p>
