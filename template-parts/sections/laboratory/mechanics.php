                    <div class="laboratory__subtitle"><span>Подготовка к работе</span></div>
                    <p>В этой формуле: m – масса шарика;</p>
                    <p>g – ускорение свободного падения;</p>
                    <p>h – высота, с которой шарик скатывается на желоб;</p>
                    <p>V – скорость, которую шарик приобретет после скатывания с горки.</p>
                    <p>Скорость, вызванная приобретенной кинетической энергией, будет постоянной и одинаковой в каждом очередном пуске, поскольку движению шарика ничто не препятствует, кроме небольшого трения</p>
                    <p>Подготовьте секундомер.</p>
