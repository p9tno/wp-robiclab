                    <div class="laboratory__subtitle"><span>Подготовка к работе</span></div>
                    <p>Равномерное прямолинейное движение удастся получить, если шарику сообщить начальную порцию энергии, одинаковую для нескольких следующих один за другим пусков.</p>
                    <p>Для этого предлагается прижать шарик рукой к выступу стартовой горки, установленной в начале желоба, как показано на рисунке. При этом шарик будет стартовать с высоты h, приобретя по отношению к уровню, на котором он будет катиться, начальную потенциальную энергию mgh.</p>
                    <p>Скатившись на желоб с высоты h, потенциальная энергия превратится в кинетическую энергию движения в соответствии с формулой: <b>mgh = mv<sup>2</sup>/2</b></p>
                  </div>
