                    <p>Удерживая шарик пальцами, опустите его на желоб и прижмите к выступу горки. Сдвиньте шкалу так, чтобы ее нулевая отметка совпала с центром шарика.</p>
                    <p>Осторожно освободите шарик. Скатившись с горки, он разгонится и покатится дальше с постоянной скоростью до удара о препятствие в виде латунного цилиндра 4, помещенного в желоб на таком расстоянии, чтобы центр тяжести шарика прокатился по желобу до остановки 180 мм от точки старта.</p>
