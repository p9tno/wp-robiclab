                    <p>На рисунке показана схема эксперимента (вид сверху). Наблюдатель располагается слева за дифракционной решеткой. Его взгляд сквозь зону решетки, содержащую 75 линий на миллиметр, направляется на прорезь в линейке, совпадающую с нулем шкалы, нанесенной на линейку.</p>
                    <p>Одновременно различаются деления шкалы и расположенные на ее фоне интерференционные всплески оранжевого цвета, первые из которых находятся на расстояниях ±х симметрично по обе стороны от щели, вторые, более бледные, на расстояниях ±2х, и так далее - теоретически до состояния, при котором угол φ станет равным 90°.</p>
