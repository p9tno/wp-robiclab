                <p>Рабочая зона решетки разбита на четыре участка с числом линий на 1 мм 50, 75, 300 и 600.</p>
                <p>Каждая линия состоит из двух линий - прозрачной для света и непрозрачной. Располагаются линии вертикально для рис. 4 (а). Именно в такой ориентации решетка должна устанавливаться в прибор. В лабораторной работе удобно использовать зону с числом линий 75. Следовательно, прибор должен быть отрегулирован так, чтобы центральный луч света проходил через эту зону.</p>
                <p>Регулировка достигается установкой решетки на рабочую позицию относительно наблюдателя в положении, показанном на рис. 4 (а). Уточненное положение решетки получают ее сдвигом в горизонтальном направлении до совпадения линии наблюдения со щелью в линейке. При этом дифракционная картина должна быть симметрична относительно щели.</p>
