                <p>Вдоль продольной направляющей с небольшим трением перемещаются две опоры 4 и 5. На опоре 4 с помощью магнита крепится дифракционная решетка, а на опоре 5 фиксируется линейка с прорезью и шкалой.</p>
                <p>Платформа 6 служит для устойчивого и точного размещения одного из трех источников света: неоновой лампы, лампы накаливания или светоизлучающего диода.</p>
