                <div class="laboratory__subtitle"><span>Краткое описание</span></div>
                <p>Для расчета длины волны излучения λ используют формулу:</p>
                <div class="center"><b>λ=±(d∙sinφ)/m /1/</b></div>
                <p>где d- постоянная дифракционной решетки, равная общей ширине прозрачной и непрозрачной полос, выраженной в миллиметрах. Для нашего случая:</p>
                <div class="center"><b>d= 1 мм : 75- 0,0133 мм;</b></div>
                <p>m-1, 2, 3...- порядки дифракционных максимумов. Для максимумов, ближайших к щели, m=1.</p>
                <p>Максимумы второго и следующих порядков рассмотреть сложно из-за ограниченной яркости источника света. Функцию sin φ для вычисления длины волны рассчитывают по формуле:</p>
                <div class="center"><b>sinφ=x/√(x^2+L^2 ) /2/</b></div>
                <p>где х- отклонение первого дифракционного максимума от середины щели до середины его изображения (х=± 12,5 мм);</p>
                <p>L- расстояние от дифракционной решетки до шкалы (L= 285 мм).</p>
