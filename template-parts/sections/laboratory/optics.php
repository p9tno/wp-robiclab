                  <p>Сместите собирающую линзу к экрану на несколько сантиметров, а затем сместите от экрана, от первоначального положения тоже на несколько сантиметров. Изображение теряет контрастноть и начинает размываться, начинается отрыв внутреннего контрасного изображения от размытого.</p>
                  <p>Самая высокая контрастность размытого изображения и его минимальный размер при контрастном внутреннем изображении и может соответствовать положению собирающей линзы в фокусе от экрана.</p>
      
