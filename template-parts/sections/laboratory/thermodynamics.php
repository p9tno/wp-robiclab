                <div class="laboratory__subtitle"><span>Краткое описание</span></div>
                <p>Основной характеристикой любого материального тела с точки зрения термодинамики является его внутренняя энергия U. Изменить внутреннюю энергию тела можно двумя путями: совершив работу:</p>
                <div class="center"><b>±∆U=±∆A</b></div>
                <p>или сообщив телу некоторое количество теплоты:</p>
                <div class="center"><b>±∆U=±∆Q.</b></div>
                <p>Учитывая, что эти два процесса обычно происходят одновременно, можно записать:</p>
                <div class="center"><b>±∆U=±∆Q=±∆A</b></div>
                <p>Это уравнение представляет собой закон сохранения в самом общем виде или первый закон термодинамики.</p>
              </div>
            </div>
            <!-- end page -->

            <!-- start page -->
            <div class="laboratory__page" data-p="">
              <div class="laboratory__content">
                <div class="laboratory__subtitle"><span>Краткое описание</span></div>
                <p>Физически этот закон означает, что любое изменение внутренней энергии тела сводится к увеличению или уменьшению содержащейся в нем теплоты, а также количеству работы, совершенной телом над внешними телами или работы, совершенной внешними телами над телом. Этим переходам энергии условно приписывают знак плюс или минус, показывая тем самым направление перехода энергии от тела к внешним телам или к телу от внешних тел.</p>
                <p>При этом внутренняя энергия тела увеличивается или уменьшается. Других причин, вызывающих изменение внутренней энергии тела, не существует.</p>
                <p>Рассмотрим подробнее воздействие на тело теплоты. Этот процесс осуществляется в виде трех явлений: теплопроводности, конвекции и излучения.</p>
              </div>
