                        <p>Воздействие излучения ускоряет движение атомов и молекул вещества, из которого состоит тело. Внутренняя энергия тела увеличивается оно нагревается. Особенностью этого способа передачи энергии - является отсутствие посредников твердых, жидких или газообразных тел. Излучение передается том случае, когда никаких посредников нет, то есть в полном вакууме, где совершенно невозможны первые два вида теплопередачи. Примером служит солнечное излучение.</p>
                        <p>В нашем случае в стакане находится горячая вода, а окружающий воздух имеет более низкую температуру - комнатную. Стенки стакана приобретают температуру воды и нагревают близкий к стеклу или металлу слой воздуха. Возникает конвекция подъем вверх нагретого воздуха.</p>
